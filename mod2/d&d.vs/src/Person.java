public class Person {
    private String name;
    private int age;
    private int[] str;
    private int[] dex;
    private int[] itl;
    private int[] wis;
    private int[] car;

    public Person() {
        
    }

    public String getName() {
        return this.name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getAge() {
        return this.age;
    }

    public void setAge(int age) {
        this.age = age;
    }

    public int[] getStr() {
        return this.str;
    }

    public void setStr(int[] str) {
        this.str = str;
    }

    public int[] getDex() {
        return this.dex;
    }

    public void setDex(int[] dex) {
        this.dex = dex;
    }

    public int[] getItl() {
        return this.itl;
    }

    public void setItl(int[] itl) {
        this.itl = itl;
    }

    public int[] getWis() {
        return this.wis;
    }

    public void setWis(int[] wis) {
        this.wis = wis;
    }

    public int[] getCar() {
        return this.car;
    }

    public void setCar(int[] car) {
        this.car = car;
    }

}
